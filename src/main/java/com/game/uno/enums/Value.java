package com.game.uno.enums;

/**
 * Enumeration representing the values of the UNO cards.
 * The enum constants include ZERO to NINE for numbered cards,
 * SKIP, REVERSE, and DRAW_TWO for special action cards,
 * and WILD and WILD_DRAW_FOUR for wild cards.
 */
public enum Value {
    ZERO, ONE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE,
    SKIP, REVERSE, DRAW_TWO, WILD, WILD_DRAW_FOUR

}
